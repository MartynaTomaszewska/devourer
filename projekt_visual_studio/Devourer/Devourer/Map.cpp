void Map::createGhosts(int ghostsNum)
{
	ghostsNumber = ghostsNum;

	ghosts = new Ghost[ghostsNumber];
	for (int i = 0; i < ghostsNumber; i++)
	{
		ghosts[i].setColor(1.0f, 0.0f, 0.0f);
		ghosts[i].setMap(this);
		ghosts[i].setWindowSize(windowWidth, windowHeight);
		ghosts[i].setSize(tileWidth, tileHeight);

		Point randomPosition;
		while(true)
		{
			randomPosition = Point(rand() % width, rand() % height);
			if (isPositionEmpty(randomPosition))
			{
				break;
			}
		}

		ghosts[i].setPosition(randomPosition);
	}
}

void Map::moveGhosts()
{
	for (int i = 0; i < ghostsNumber; i++)
	{
		ghosts[i].runFromDevourer();
	}
}

bool Map::isPositionEmpty(const Point& position)
{
	if (tiles[position.x][position.y].type != EMPTY)
	{
		// na tej pozycji jest sciana
		return false;
	}

	for(int i = 0; i < ghostsNumber; i++)
	{
		if (position.x == ghosts[i].getPosition().x && position.y == ghosts[i].getPosition().y)
		{
			// na tej pozycji jest duch
			return false;
		}
	}

	if (position.x == player->getPosition().x && position.y == player->getPosition().y)
	{
		// na tej pozycji jest gracz
		return false;
	}

	return true;
}

Map::~Map()
{
	for (int i = 0; i < width; i++)
	{
		delete [] tiles[i];
	}
	delete [] tiles;
	delete [] ghosts;
	delete player;
}

void Map::draw()
{
	for (int i = 0; i < width; i++)
	{
		for (int j = 0; j < height; j++)
		{
			tiles[i][j].draw();
		}
	}

	for(int i = 0; i < ghostsNumber; i++)
	{
		ghosts[i].draw();
	}

	player->draw();
}

bool Map::canObjectMove(GameObject* object, MoveDirection direction)
{
	switch(direction)
	{
	case UP:
		if (((object->getPosition().y + 1) < height) && (tiles[object->getPosition().x][object->getPosition().y + 1].type == EMPTY))
		{
			return true;
		}
		break;
	case DOWN:
		if (((object->getPosition().y - 1) >= 0) && (tiles[object->getPosition().x][object->getPosition().y - 1].type == EMPTY))
		{
			return true;
		}
		break;
	case LEFT:
		if (((object->getPosition().x - 1) >= 0) && (tiles[object->getPosition().x - 1][object->getPosition().y].type == EMPTY))
		{
			return true;
		}
		break;
	case RIGHT:
		if (((object->getPosition().x + 1) < width) && (tiles[object->getPosition().x + 1][object->getPosition().y].type == EMPTY))
		{
			return true;
		}
		break;
	}

	return false;
}

Ghost* Map::getGhostAtPosition(const Point& position)
{
	for(int i = 0; i < ghostsNumber; i++)
	{
		if (position.x == ghosts[i].getPosition().x && position.y == ghosts[i].getPosition().y)
		{
			return &ghosts[i];
		}
	}
	return NULL;
}

void Map::removeGhost(Ghost* ghostToRemove)
{
	Ghost* oldGhosts = ghosts;
	int oldGhostsNumber = ghostsNumber;

	ghostsNumber--;

	if (ghostsNumber > 0)
	{
		ghosts = new Ghost[ghostsNumber];

		int j = 0;
		for(int i = 0; i < oldGhostsNumber; i++)
		{
			if (&oldGhosts[i] != ghostToRemove)
			{
				ghosts[j] = oldGhosts[i];
				j++;
			}
		}
	}

	delete [] oldGhosts;
}